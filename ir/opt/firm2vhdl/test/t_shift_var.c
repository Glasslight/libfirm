uint32_t test_atom(uint8_t control, uint32_t input0, uint32_t input1)
{
  if (control == 1) {
    return input0 << input1;
  } else if (control == 2){
    return input0 >> input1;
  } else {
    return ((int32_t)input0) >> input1;
  }
}

